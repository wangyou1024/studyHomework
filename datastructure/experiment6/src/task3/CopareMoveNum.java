package task3;

public class CopareMoveNum {
    private int cpn; //比较次数
    private int mvn;    //移动次数

    public CopareMoveNum() {
        this.cpn = 0;
        this.mvn = 0;
    }

    public int getCpn() {
        return cpn;
    }

    public void setCpn(int cpn) {
        this.cpn = cpn;
    }

    public int getMvn() {
        return mvn;
    }

    public void setMvn(int mvn) {
        this.mvn = mvn;
    }
}
